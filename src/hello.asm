#include    include/bios.inc
#include    include/kernel.inc
#include    include/ops.inc
#include    include/vdp.inc


#define cr  13
#define lf  10

; declare external procedures in vdp_video library
        extrn  checkVideo
        extrn  setAddress
        extrn  beginTextMode
        extrn  setTextCharXY
        extrn  writeTextString
        extrn  endTextMode


; Executable program header generated by linker

        org     2000h
hello:      br      main
            
            ; Build information            
            db      9+80h              ; month
            db      1                 ; day
            dw      2022               ; year
            dw      2                  ; build
            
            db      'Copyright 2022 by Gaston Williams',0
            
main:       call checkVideo     ; verify vdp driver is loaded in memory
            lbdf no_driver
            
            call beginTextMode  ; start text mode
            
            call setTextCharXY
            db   10, 0
            mov  rf, GREETING 
            call writeTextString

            ldi V_VDP_KEEP     ; reset the display
            call endTextMode
            rtn                ; return to monitor


no_driver:  call O_INMSG
            db 'TMS9X18 Video driver is not loaded.',10,13,0
            rtn

GREETING:  db 'Hello, World!', 0       ; 13 chars

            end hello
