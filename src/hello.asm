;            Text mode memory map
;            +-----------------+ 0000h
;            |  Pattern Table  |
;            +-----------------+ 0800h 2048 (can define 256 codes)
;            |    Name Table   |
;            +-----------------+ 0BC0h 3008 (40x24 960 chars)
;            |     Unused      |
;            +-----------------+ 3FFFh (16k VRAM)
