              extrn  beginG2Mode
              extrn  sendBitmap
              extrn  sendColors
              extrn  sendNames
              extrn  endG2Mode

              ; Executable program header generated by linker
          
          org   2000h
showPalette:  br      main                        
                        
              ; Build information
                        
