            ldi  0
            phi  ra            ; treated below as 8 bit register
            phi  rb            ;          ..   
LPIX:       lda  rf            ; get coordinate pair ra.0, rb.0
            plo  ra            ; X
            lda  rf
            plo  rb            ; Y

            call CALC_PIXEL    ; r7 has address, rc.0 the byte mask
            ldn  r7            ; get existing pixel pattern in memory
            str  2             ; save for 'or' operation
            glo  rc            ; get new pixel pattern
            or                 ; 'or' with original
            str  r7            ; save in memory device context
            
            ; check if done
            dec  r9
            dec  r9            ; since we have pairs of data
            glo  r9
            bnz  LPIX
            ghi  r9
            bnz  LPIX

            call sendBitmap    ; blit the plot to screen
            dw START_BITMAP
            
            ; Annotate plot
            ldi  02 
            plo  r9             ; set x = 2
            ldi  03          
            phi  r9             ; set y = 3
            call setG2CharXY   ; convert co-ords from char to graphics address
            mov  rf, TITLE_BUF
            call drawG2String
              
            ldi  19 
            plo  r9             ; set x = 19
            ldi  03          
            phi  r9             ; set y = 3            
            call setG2CharXY
            mov  rf, PLOT_NAME
            call drawG2String

            ldi  19 
            plo  r9             ; set x = 19
            ldi  04          
            phi  r9             ; set y = 4                        
            call setG2CharXY
            mov  rf, PLOT_RANGE
            call drawG2String
            
            ; force refresh of display
            call updateG2Mode
            ; set Exapnsion Group back to default
            
            ldi  V_VDP_KEEP
            call endG2Mode
            rtn
            
