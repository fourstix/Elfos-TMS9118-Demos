            dec  r7
            out  VDP_DAT_P
            ghi  r7
            out  VDP_DAT_P
            lbnz NEXT_BYTE
            sex  r2
            rtn
            
; -----------------------------------------------------------
; Set name table entries of vram @ 3800h (Name table)
