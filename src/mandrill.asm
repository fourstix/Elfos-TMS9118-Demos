            lda  r6
            plo  rf          ; rf has address from linkage
            str  r2
            out  VDP_REG_P   ; send low byte of address
            dec  r2
            ghi  rf
            ani  03fh        ; set two msb's of high byte address to zero
            str  r2
            out  VDP_REG_P   ; and then high byte
            dec  r2
            rtn

